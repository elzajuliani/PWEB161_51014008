<h1> Biografi Singkat Presiden Joko Widodo</h1>
<hr>
</center>

<h4>Jokowi menempuh pendidikan dasar hingga menengah di kota Solo. Dan setelah itu pada pendidikan tinggi, Jokowi memilih untuk belajar di UGM. Pada saat menempuh pendidikan di perguruan tinggi memang tidak ada prestasi yang menonjol yang dilakukan oleh Jokowi. Setelah selesai kuliah, pria ini memilih untuk bekerja pada sebuah perusahaan. Akan tetapi dia tidak bertahan lama dan memilih untuk meneruskan usaha mebel yang dimiliki oleh keluarga. Dalam waktu yang cepat, usaha mebel yang dilakukan berhasil mendapatkan banyak keuntungan.<br>
<br>

Pada tahun 2005 Jokowi terpilih menjadi walikota Solo. Ada banyak sekali prestasi yang sudah dilakukan pria ini di kota Solo. Karena melihat prestasi Jokowi yang bagus di kota Solo, pada tahun 2012 dia dicalonkan menjadi gubernur Jakarta.<br>
<br>

Setelah menjadi gubernur Jakarta, tentu membuat pria ini semakin sibuk. Banyak sekali aktifitas yang harus dia lakukan. Tidak sedikit pula media yang membuat berita khusus akan Jokowi. Dengan popularitas yang semakin meningkat ini sudah pasti membuat banyak orang yang ingin Jokowi maju sebagai presiden.
Setelah melalui serangkaian pemilu yang panjang, akhirnya jokowi terpilih sebagai Presiden Indonesia  ke 7 peroide 2014-2019.<br>
